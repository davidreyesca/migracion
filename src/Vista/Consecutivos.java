    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        int seleccionado = jVerificar.getSelectedIndex();
        String buscarPor=jVerificar.getItemAt(seleccionado);
        
        if(incial.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Tienes que llenar el campo incial");
        }else if(Final.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Tienes que llenar el campo final");
        }else
        {
            if(buscarPor.equals("Número de expediente"))
            {
                LimpiarTabla();
                BuscarPorExpediente();
            }else
            {
                LimpiarTabla();
                BuscarPorInstrumento();
            }
        }
    }//GEN-LAST:event_jBuscarActionPerformed
