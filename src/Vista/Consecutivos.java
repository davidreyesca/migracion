    public void JalarDatosInstrumento(int noInicial, int noFinal)
    {
        ConexionMySql mysql = new ConexionMySql();
        Connection cn = mysql.getConection();
        String sSQL="SELECT Instrumento FROM compraventa WHERE Instrumento >='" + noInicial + "' AND Instrumento <='" + noFinal + "' ORDER BY Instrumento";
        try 
        {
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sSQL);
                while(rs.next()) 
                {
                        arrayListInstrumentos.add(rs.getInt("Instrumento"));
                }
        }catch (SQLException ex) {
                System.out.println("Error al obtener todos los expedientes" + ex);
        }finally
            //Cuando se termine todo el proceso cierra la conexión y manda llamar al metodo Inicio.
        {
                mysql.desconectar();
        }
    }
